---utilities.strings.formatters.add(FakeXML,"b",[["<" ..%s..">" ]])
---utilities.strings.formatters.add(FakeXML,"e",[["</"..%s..">" ]])
---utilities.strings.formatters.add(FakeXML,"n",[["<" ..%s.."/>"]])
---
---context(FakeXML["It looks like %!b!xml%!e! doesn't it?"]("it","it"))
---\stopbuffer
---
---\typebuffer
---
